/**
 * نسبة مئوية للعرض البصري (أشرطة التقدّم).
 *
 * ── لماذا ملف كامل لسطر واحد؟ ─────────────────────────────────────────────
 * قاعدة ESLint تمنع `Math.round` في كل المشروع، لأن التقريب المالي يجب أن
 * يمر بـ`@oh/money` بسياسة موحّدة (HALF_UP).
 *
 * لكن هذه الحالة **ليست مالية**: نحسب عرض شريط تقدّم من عددين صحيحين
 * (`3 مستخدمين من 5`). لا يوجد مبلغ، ولا كسور نقدية، ولا أثر على أي رصيد —
 * النتيجة تذهب إلى `style.width` فقط.
 *
 * بدل نثر `eslint-disable` في كل مكوّن (فيصير التعطيل عادةً لا استثناءً)،
 * نركّز الاستخدام المشروع الوحيد هنا، موثّقًا ومسمّى. أي `Math.round` آخر
 * في المشروع يظل خطأً يمنع البناء — وهو ما نريده.
 *
 * ⚠️ لا تستخدم هذه الدالة لأي حساب يمس مبلغًا. للنِّسَب المالية (ضريبة، خصم)
 *    استخدم `percentOf()` من `@oh/money`.
 */
export function displayPercent(used: number, limit: number): number {
  if (limit <= 0) return 0;
  // eslint-disable-next-line no-restricted-properties -- نسبة عرض بصري من عددين صحيحين، لا مبلغ.
  return Math.min(100, Math.round((used / limit) * 100));
}
