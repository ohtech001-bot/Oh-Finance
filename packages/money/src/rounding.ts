import { Decimal } from 'decimal.js';

/**
 * سياسة التقريب المالي الموحّدة للنظام بأكمله.
 *
 * ── لماذا HALF_UP وليس بنك (HALF_EVEN)؟ ─────────────────────────────────────
 * التقريب المصرفي (half-even / banker's rounding) يقرّب 0.5 إلى أقرب زوجي،
 * فيعطي: 1.005 → 1.00  و  1.015 → 1.02. هذا سلوك صحيح إحصائيًا لكنه يخالف
 * توقّع التاجر والزبون، ويصعب شرحه في نزاع على فاتورة.
 *
 * HALF_UP (نصف لأعلى) هو العرف التجاري في الفوترة: 1.005 → 1.01 دائمًا.
 * وهو ما تتوقعه الجهات الضريبية في المنطقة.
 *
 * ⚠️ هذا القرار مركزي عمدًا: لا يوجد مكان آخر في النظام يقرّب مبلغًا.
 *    تغييره هنا يغيّر النظام كله بلا انحراف بين وحدة وأخرى.
 * ─────────────────────────────────────────────────────────────────────────────
 */
export const ROUNDING_MODE = Decimal.ROUND_HALF_UP;

/**
 * ضبط عام لـ decimal.js.
 *
 * precision: 34 خانة معنوية — يكفي بفارق هائل لأي مبلغ تجاري، ويمنع
 * أخطاء التقريب في العمليات الوسيطة (خاصة القسمة عند توزيع دفعة).
 * toExpNeg/toExpPos: نوسّع الحدود كي لا تنقلب المبالغ إلى صيغة أسّية
 * ("1e-7") عند التحويل إلى نص — وهي صيغة تكسر NUMERIC في PostgreSQL.
 */
Decimal.set({
  precision: 34,
  rounding: ROUNDING_MODE,
  toExpNeg: -15,
  toExpPos: 30,
});

export { Decimal };
